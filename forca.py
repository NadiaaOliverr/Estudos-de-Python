def carrega_palavra_secreta(primeira_linha_valida=0, nome_arquivo="palavras.txt"):
    arquivo = open(nome_arquivo, "r")
    palavras = []
    for linha in arquivo:
        linha = linha.strip()
        palavras.append(linha)

    arquivo.close()

    palavra_selecionada = randrange(primeira_linha_valida, len(palavras))
    palavra_secreta = palavras[palavra_selecionada].upper()
    return palavra_secreta


def inicializa_letras_acertadas(palavra_secreta):
    return ["_" for letra in palavra_secreta]


def pede_chute():
    chute = input("\nQual letra? ")
    chute = chute.strip().upper()
    return chute


def marca_chute_correto(palavra_secreta, chute, letras_acertadas):
    index = 0
    for letra in palavra_secreta:
        if(chute == letra):
            letras_acertadas[index] = letra
        index += 1


